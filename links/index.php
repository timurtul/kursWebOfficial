    <script>
        document.getElementById('year').textContent = new Date().getFullYear();

        // Simple analytics: track clicks per link text in localStorage
        (function() {
            const STORAGE_KEY = 'linkClickCounts';
            function loadCounts() {
                try { return JSON.parse(localStorage.getItem(STORAGE_KEY)) || {}; } catch { return {}; }
            }
            function saveCounts(counts) {
                try { localStorage.setItem(STORAGE_KEY, JSON.stringify(counts)); } catch {}
            }
            function increment(label) {
                const counts = loadCounts();
                counts[label] = (counts[label] || 0) + 1;
                saveCounts(counts);
            }
            function labelForAnchor(a) {
                return (a.textContent || a.getAttribute('aria-label') || a.getAttribute('href') || 'link').trim();
            }

            const anchors = document.querySelectorAll('nav[aria-label="Primary links"] a, a[aria-label="Contact me via email"], .social-links a');
            anchors.forEach(a => {
                a.addEventListener('click', () => increment(labelForAnchor(a)), { passive: true });
            });

            // Expose counts for debugging: window.getLinkCounts()
            window.getLinkCounts = function() { return loadCounts(); };
        })();
    </script>
